export const squareAll = function (items: number[]): number[] {
  const result: number[] = [];

  for (const item of items) {
    result.push(item * item);
  }

  return result;
};

export const cubeAll = function (items: number[]): number[] {
  const result: number[] = [];

  for (const item of items) {
    result.push(item * item);
  }

  return result;
};
